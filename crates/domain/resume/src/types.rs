use rara_domain_shared::convert::{
    chrono_opt_to_timestamp, chrono_to_timestamp, timestamp_opt_to_chrono, timestamp_to_chrono,
    u8_from_i16,
};
use rara_model::resume::Resume as StoreResume;

fn resume_source_from_i16(value: i16) -> ResumeSource {
    let repr = u8_from_i16(value, "resume.source");
    ResumeSource::from_repr(repr).unwrap_or_else(|| panic!("invalid resume.source: {value}"))
}

/// Store `Resume` -> Domain `Resume`.
impl From<StoreResume> for Resume {
    fn from(r: StoreResume) -> Self {
        Self {
            id:                  r.id,
            title:               r.title,
            version_tag:         r.version_tag,
            content_hash:        r.content_hash,
            source:              resume_source_from_i16(r.source),
            content:             r.content,
            parent_resume_id:    r.parent_resume_id,
            target_job_id:       r.target_job_id,
            customization_notes: r.customization_notes,
            tags:                r.tags,
            metadata:            r.metadata,
            pdf_object_key:      r.pdf_object_key,
            pdf_file_size:       r.pdf_file_size,
            trace_id:            r.trace_id,
            is_deleted:          r.is_deleted,
            deleted_at:          chrono_opt_to_timestamp(r.deleted_at),
            created_at:          chrono_to_timestamp(r.created_at),
            updated_at:          chrono_to_timestamp(r.updated_at),
        }
    }
}

/// Domain `Resume` -> Store `Resume`.
impl From<Resume> for StoreResume {
    fn from(r: Resume) -> Self {
        Self {
            id:                  r.id,
            title:               r.title,
            version_tag:         r.version_tag,
            content_hash:        r.content_hash,
            source:              r.source as u8 as i16,
            content:             r.content,
            parent_resume_id:    r.parent_resume_id,
            target_job_id:       r.target_job_id,
            customization_notes: r.customization_notes,
            tags:                r.tags,
            metadata:            r.metadata,
            pdf_object_key:      r.pdf_object_key,
            pdf_file_size:       r.pdf_file_size,
            trace_id:            r.trace_id,
            is_deleted:          r.is_deleted,
            deleted_at:          timestamp_opt_to_chrono(r.deleted_at),
            created_at:          timestamp_to_chrono(r.created_at),
            updated_at:          timestamp_to_chrono(r.updated_at),
        }
    }
}

impl axum::response::IntoResponse for ResumeError {
    fn into_response(self) -> axum::response::Response {
        let status = match &self {
            ResumeError::NotFound { .. } => axum::http::StatusCode::NOT_FOUND,
            ResumeError::InvalidContent { .. } => axum::http::StatusCode::BAD_REQUEST,
            ResumeError::ParentNotFound { .. } => axum::http::StatusCode::BAD_REQUEST,
            ResumeError::DuplicateContent { .. } => axum::http::StatusCode::CONFLICT,
            ResumeError::FileTooLarge { .. } => axum::http::StatusCode::PAYLOAD_TOO_LARGE,
            ResumeError::InvalidFileType { .. } => axum::http::StatusCode::UNSUPPORTED_MEDIA_TYPE,
            ResumeError::UploadFailed { .. } => axum::http::StatusCode::INTERNAL_SERVER_ERROR,
            ResumeError::PdfNotFound { .. } => axum::http::StatusCode::NOT_FOUND,
            ResumeError::Storage { .. } => axum::http::StatusCode::INTERNAL_SERVER_ERROR,
        };
        let message = self.to_string();
        if status.is_server_error() {
            tracing::error!(http_status = status.as_u16(), error = %message, "resume request error");
        }
        let body = serde_json::json!({
            "error": { "status": status.as_u16(), "message": message }
        });
        (status, axum::Json(body)).into_response()
    }
}
